#include <stdio.h>

int main(void)
{

  /* Declare
   * the
   * variables
   */
  float x = 5;
  float y = 2;

  /* Display the results */
  printf ("%2.0f + %2.0f = %2.0f \n", x, y, x + y);
  printf ("%2.0f - %2.0f = %2.0f \n", x, y, x - y);
  printf ("%2.0f * %2.0f = %2.0f \n", x, y, x * y);
  printf ("%2.0f / %2.0f =  %2.1f \n", x, y, x / y);


  return 0;
}
